





def visualize_grid(grid, title=""):
    """简单可视化网格（控制台输出）"""
    print(f"\n{title}")

    # 颜色映射字符
    color_map_emoji = {
        0: "⬛",  # 背景
        1: "🟥",  # 红色
        2: "🟩",  # 绿色
        3: "🟦",  # 蓝色
        4: "🟨",  # 黄色
        5: "🟪",  # 紫色
        6: "🟦",  # 蓝色
        7: "⬜",  # 白色
        8: "🟫",  # 棕色
        9: "🟧",  # 橙色
    }

    color_map = {
        0: "⬛",   # 黑色
        1: "🟦",   # 蓝色
        2: "🟥",   # 红色
        3: "🟩",   # 绿色
        4: "🟨",   # 黄色
        5: "🟫",  # 灰色（白色中等方块代替）
        6: "🟪",   # 粉紫
        7: "🟠",   # 橙色
        8: "🔹",   # 浅蓝（大蓝菱形）
        9: "🔴",   # 酒红（复用红色方块）
    }


    for row in grid:
        print("".join(color_map.get(c, "⬜") for c in row))





